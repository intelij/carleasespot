                                        timeOut: 10000,
                                        "closeButton": true,
                                        "positionClass": "toast-bottom-right"
                                    })

                                } else

                                {

                                    toastr.error('Something went wrong, please try it again.', 'Error!', {
                                        timeOut: 15000,
                                        "closeButton": true,
                                        "positionClass": "toast-bottom-right"
                                    })

                                }

                            });

                    }

                });

